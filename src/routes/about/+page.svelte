<h1>About EchoVerse</h1>

<p>
	EchoVerse is a fictional chatting platform for demonstrating ChatGPT-powered bots and
	applications. It was created by Quangdao Nguyen for April Fools' 2024 at nvisia.
</p>

<h2>Branding</h2>

<p>
	The branding for EchoVerse was created entirely through artificial intelligence. The name itself,
	EchoVerse, was conceived by ChatGPT 3.5, while the logo and color scheme was made using
	<a href="https://brandmark.io/">BrandMark</a>. Any affiliation with a real brand is entirely
	unintentional.
</p>
